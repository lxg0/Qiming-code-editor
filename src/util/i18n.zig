const std = @import("std");

pub const Locale = enum {
    zh_CN,
    zh_TW,
    en_US,
    ja_JP,
    ko_KR,
};

pub const I18n = struct {
    allocator: std.mem.Allocator,
    locale: Locale,

    pub fn init(allocator: std.mem.Allocator) I18n {
        return I18n{ .allocator = allocator, .locale = .zh_CN };
    }

    pub fn tr(self: *const I18n, key: []const u8) []const u8 {
        _ = self;
        return translate(key);
    }

    pub fn trf(self: *const I18n, comptime fmt: []const u8, args: anytype) []const u8 {
        return std.fmt.allocPrint(self.allocator, translate(fmt), args) catch unreachable;
    }

    pub fn setLocale(self: *I18n, locale: Locale) void {
        self.locale = locale;
    }
};

